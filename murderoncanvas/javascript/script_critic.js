function toggle_comments01() {
  var cont = document.getElementById('comments_all01');
  if (cont.style.display == 'block') {
      cont.style.display = 'none';
      document.getElementById("comments_title01").innerHTML = "Show comments";
  }
  else {
      cont.style.display = 'block';
      document.getElementById("comments_title01").innerHTML = "Hide comments";
  }
}

